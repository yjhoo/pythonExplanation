# 송아지 찾기(BFS : 상태트리탐색)
# 현수는 송아지를 잃어버렸다. 다행히 송아지에는 위치추적기가 달려 있다. 현수의 위치와 송아 지의 위치가 직선상의 좌표 점으로 주어지면 현수는 현재 위치에서 송아지의 위치까지 다음과 같은 방법으로 이동한다. 현수는 스카이 콩콩을 타고 가는데 한 번의 점프로 앞으로 1, 뒤로 1, 앞으로 5를 이동할 수 있다. 최소 몇 번의 점프로 현수가 송아지의 위치까지 갈 수 있는지 구하는 프로그램을 작성 하세요.
# ▣ 입력설명 첫 번째 줄에 현수의 위치 S와 송아지의 위치 E가 주어진다. 직선의 좌표 점은 1부터 10,000 까지이다.
# ▣ 출력설명 점프의 최소횟수를 구한다.
# ▣ 입력예제 1                                   5 14
# ▣ 출력예제 1 3

import sys
import math

#sys.stdin=open("in3.txt", "r")

# v - 피봇, t - 진행된 총 횟수의 합
def f_check(v, t):
    global cnt
    if v == E:
        if cnt > t:
            cnt = t
    elif v > E:
        f_check(v-1, t+1)
    else:
        t += (E - v) // 5
        v += (E - v) - (E - v) % 5
        # 횟수가 위의 과정에서 맞춰졌을 수도 있으므로 한번 더 추가
        if v == E:
            if cnt > t:
                cnt = t
        f_check(v+1, t+1)
        f_check(v+5, t+1)
S, E = map(int, input().split())
cnt = 10000
f_check(S, 0)
print(cnt)
